"""
字符串的三种定义方式
"""
# 单引号定义
name = '未来可期'
print(name)
print(type(name))
# 双引号定义
name = "未来可期"
print(name)
print(type(name))
# 三引号定义
name = """未来可期"""
print(name)
print(type(name))
# 双引号定义字符串可以包括单引号
name = "'未来可期'"
print(name)
print(type(name))
# 使用转义字符\接触引号的效用
name = "\"未来可期"
print(name)
name = '\'未来可期'
print(name)



