"""
数据转换
"""
a = 1
b = 1.0
c = "1"
print("原始")
print(a)
print(b)
print(c)
a = int(a)
b = int(b)
c = int(c)
print("int型")
print(a,type(a))
print(b,type(b))
print(c,type(c))
a = float(a)
b = float(b)
c = float(c)
print("float型")
print(a,type(a))
print(b,type(b))
print(c,type(c))
a = str(a)
b = str(b)
c = str(c)
print("str型")
print(a,type(a))
print(b,type(b))
print(c,type(c))
