"""
演示bool类型的定义
以及比较运算符的应用
"""
# 定义变量储存bool类型的数据
bool_1 = True
bool_2 = False
print(f"bool_1变量的内容是：{bool_1}储存的数据类型是：{type(bool_1)}")
print(f"bool_2变量的内容是：{bool_2}储存的数据类型是：{type(bool_2)}")

# 比较运算符的使用
num1=10
num2=10
num3=10
num4=15
print(f"num1 == num2的结果是{num1==num2}")
print(f"num3 != num4的结果是{num3!=num4}")